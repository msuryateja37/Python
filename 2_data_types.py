# integer type
x = 5 
print(type(x),"\n")

# float type
a = 5.5
print(type(a),"\n")

# String type
s = "Hello Surya"
print(s,"\n",type(s),"\n")

# boolen type
mybool = True 
print(type(mybool),"\n")

# complex type
c = 1j
print(type(c),"\n")

# list type
mylist = ["apple" , "banana" , "cherry" ]
print(mylist)
print(type(mylist),"\n")

# tuple type
mytup =("apple" , "banana" , "cherry")
print(mytup,"\n",type(mytup),"\n")

# Rnange type
x= range(5)
print(x,"\n",type(x),"\n")

# Dictionary type
dict = { "1":"surya" , "2":"teja" }
print(dict,"\n",type(dict),"\n")


# Type conversion in python
mybool = 1 # this is integer type
print(type(bool(mybool))) # conversion into bool
